function ensureReadingModal() {
  if (document.getElementById("reading-modal")) {
    return;
  }

  const modal = document.createElement("div");
  modal.id = "reading-modal";
  modal.className = "reading-modal hidden";
  modal.innerHTML = `
    <div class="reading-modal-card">
      <div class="reading-modal-head">
        <h3 id="reading-modal-title">Detaylı İçerik</h3>
        <button id="close-reading-modal" class="close-modal-btn" type="button" aria-label="Kapat">×</button>
      </div>
      <div id="reading-modal-body" class="reading-modal-body"></div>
    </div>
  `;
  document.body.appendChild(modal);

  const closeBtn = document.getElementById("close-reading-modal");
  closeBtn.addEventListener("click", closeReadingModal);
  modal.addEventListener("click", (event) => {
    if (event.target === modal) {
      closeReadingModal();
    }
  });
}

function openReadingModal(title, content) {
  const modal = document.getElementById("reading-modal");
  const titleNode = document.getElementById("reading-modal-title");
  const bodyNode = document.getElementById("reading-modal-body");
  if (!modal || !titleNode || !bodyNode) {
    return;
  }
  titleNode.textContent = title;
  bodyNode.textContent = content;
  modal.classList.remove("hidden");
}

function closeReadingModal() {
  const modal = document.getElementById("reading-modal");
  if (!modal) {
    return;
  }
  modal.classList.add("hidden");
}

function setupReadableCards() {
  document.querySelectorAll(".read-item").forEach((item) => {
    if (item.querySelector(".read-more-btn")) {
      return;
    }
    const titleNode = item.querySelector("h3");
    const summaryNode = item.querySelector("p");
    if (!titleNode || !summaryNode) {
      return;
    }
    const fullText =
      item.dataset.readContent ||
      `${summaryNode.textContent} Bu içerik ilgili bölümde güncel olarak detaylandırılmaktadır.`;
    const button = document.createElement("button");
    button.type = "button";
    button.className = "read-more-btn";
    button.textContent = "Daha Fazla Oku";
    button.dataset.title = titleNode.textContent.trim();
    button.dataset.content = fullText;
    summaryNode.classList.add("summary-text");
    item.appendChild(button);
  });
}

function setupReadMoreEvents() {
  document.addEventListener("click", (event) => {
    const btn = event.target.closest(".read-more-btn");
    if (!btn) {
      return;
    }
    openReadingModal(btn.dataset.title || "Detay", btn.dataset.content || "");
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeReadingModal();
    }
  });
}

function renderHomePage() {
  const newsSlider = document.getElementById("news-slider");
  const prevSlideButton = document.getElementById("prev-slide");
  const nextSlideButton = document.getElementById("next-slide");
  const announcementList = document.getElementById("announcement-list");
  const quickLinksGrid = document.getElementById("quick-links-grid");

  if (
    !newsSlider ||
    !prevSlideButton ||
    !nextSlideButton ||
    !announcementList ||
    !quickLinksGrid
  ) {
    return;
  }

  let currentSlideIndex = 0;

  function renderSlide(index) {
    const slide = siteData.news[index];
    newsSlider.innerHTML = `
      <article class="slide read-item" data-read-content="${slide.fullText || slide.text}">
        <img src="${slide.image}" alt="${slide.title}">
        <div class="slide-content">
          <h3>${slide.title}</h3>
          <p class="summary-text">${slide.text}</p>
          <button class="read-more-btn" type="button" data-title="${slide.title}" data-content="${slide.fullText || slide.text}">Daha Fazla Oku</button>
        </div>
      </article>
    `;
  }

  function nextSlide() {
    currentSlideIndex = (currentSlideIndex + 1) % siteData.news.length;
    renderSlide(currentSlideIndex);
  }

  function prevSlide() {
    currentSlideIndex =
      (currentSlideIndex - 1 + siteData.news.length) % siteData.news.length;
    renderSlide(currentSlideIndex);
  }

  prevSlideButton.addEventListener("click", prevSlide);
  nextSlideButton.addEventListener("click", nextSlide);
  setInterval(nextSlide, 5500);
  renderSlide(currentSlideIndex);

  announcementList.innerHTML = siteData.announcements
    .map(
      (item) => `
        <li>
          <span>${item}</span>
          <button class="read-more-btn" type="button" data-title="Duyuru Detayı" data-content="${item}">
            Daha Fazla Oku
          </button>
        </li>
      `
    )
    .join("");

  quickLinksGrid.innerHTML = siteData.quickLinks
    .map(
      (link) =>
        `<a class="quick-link" href="${link.href}">${link.label}</a>`
    )
    .join("");
}

