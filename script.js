function renderSharedLayout() {
  const currentPage = document.body.dataset.page || "";
  const headerTarget = document.getElementById("site-header");
  const footerTarget = document.getElementById("site-footer");
  const navLinks = pageMap
    .map((item) => {
      const activeClass = currentPage === item.key ? "active" : "";
      return `<a class="${activeClass}" href="${item.href}">${item.label}</a>`;
    })
    .join("");

  if (headerTarget) {
    headerTarget.innerHTML = `
      <header class="site-header">
        <div class="container nav-wrapper">
          <a class="brand" href="index.html">
            <span class="brand-mark">M</span>
            <span class="brand-text">Mevlana Kız AİHL</span>
          </a>
          <button
            id="menu-toggle"
            class="menu-toggle"
            aria-label="Menüyü aç veya kapat"
            aria-expanded="false"
          >
            <span></span><span></span><span></span>
          </button>
          <nav id="main-nav" class="main-nav" aria-label="Ana menü">${navLinks}</nav>
        </div>
      </header>
    `;
  }

  if (footerTarget) {
    footerTarget.innerHTML = `
      <footer class="site-footer">
        <div class="container">
          <p>&copy; 2026 Mevlana Kız Anadolu İmam Hatip Lisesi - Tüm hakları saklıdır.</p>
        </div>
      </footer>
    `;
  }
}

function setupMobileMenu() {
  const menuToggle = document.getElementById("menu-toggle");
  const mainNav = document.getElementById("main-nav");
  if (!menuToggle || !mainNav) {
    return;
  }

  menuToggle.addEventListener("click", () => {
    const isOpen = mainNav.classList.toggle("open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  [...mainNav.querySelectorAll("a")].forEach((link) => {
    link.addEventListener("click", () => {
      mainNav.classList.remove("open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });
}

function setupSplash() {
  const splashScreen = document.getElementById("splash-screen");
  const skipSplashButton = document.getElementById("skip-splash");
  if (!splashScreen || !skipSplashButton) {
    return;
  }

  function closeSplash() {
    splashScreen.classList.add("hidden");
    setTimeout(() => {
      splashScreen.style.display = "none";
    }, 400);
  }

  setTimeout(closeSplash, 5000);
  skipSplashButton.addEventListener("click", closeSplash);
}

function setupAccordion() {
  const accordionButtons = document.querySelectorAll(".accordion-btn");
  accordionButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const content = button.nextElementSibling;
      if (!content) {
        return;
      }
      const isActive = content.classList.contains("active");
      document.querySelectorAll(".accordion-content").forEach((panel) => {
        panel.classList.remove("active");
      });
      if (!isActive) {
        content.classList.add("active");
      }
    });
  });
}

